export function generateProposalHTMLPreview(
  proposal: GeneratedProposal,
  project: TenderProject,
  brand: BrandProfile | null,
  locale?: PdfLocale
): Buffer {
  return Buffer.from(
    buildProposalHTML(proposal, project, brand, { forPrint: false, locale }),
    "utf8"
  );
}

/** Binary PDF via Playwright Chromium. Throws if browser cannot launch. */
export async function generateProposalPDF(
  proposal: GeneratedProposal,
  project: TenderProject,
  brand: BrandProfile | null,
  locale?: PdfLocale
): Promise<Buffer> {
  let brandForPdf = brand;
  if (brand?.logoUrl?.startsWith("/")) {
    try {
      const { readStoredFile, fileExists } = await import("./storage");
      const pathMatch = brand.logoUrl.match(/path=([^&]+)/);
      if (pathMatch) {
        const storagePath = decodeURIComponent(pathMatch[1]);
        if (await fileExists(storagePath)) {
          const bytes = await readStoredFile(storagePath);
          const ext = storagePath.split(".").pop()?.toLowerCase() ?? "png";
          const mime =
            ext === "jpg" || ext === "jpeg"
              ? "image/jpeg"
              : ext === "webp"
                ? "image/webp"
                : ext === "svg"
                  ? "image/svg+xml"
                  : "image/png";
          brandForPdf = {
            ...brand,
            logoUrl: `data:${mime};base64,${bytes.toString("base64")}`,
          };
        }
      }
    } catch {
      /* keep relative logo — may not render in PDF */
    }
  }

  const html = buildProposalHTML(proposal, project, brandForPdf, { forPrint: true, locale });
  try {
    const { chromium } = await import("playwright");
    const browser = await chromium.launch({
      headless: true,
      args: ["--no-sandbox", "--disable-setuid-sandbox"],
    });
    try {
      const page = await browser.newPage();
      await page.setContent(html, { waitUntil: "networkidle", timeout: 60_000 });
      // Allow Google Fonts to load for Arabic typography
      await page.waitForTimeout(400);
      const pdf = await page.pdf({
        format: "A4",
        printBackground: true,
        displayHeaderFooter: true,
        headerTemplate: `<div style="font-size:8px;width:100%;text-align:center;color:#94a3b8;padding:0 12mm;">Arabclue · ${escapeHtml(project.etimadRef ?? "")}</div>`,
        footerTemplate: `<div style="font-size:8px;width:100%;text-align:center;color:#94a3b8;padding:0 12mm;"><span class="pageNumber"></span> / <span class="totalPages"></span></div>`,
        margin: { top: "16mm", bottom: "18mm", left: "12mm", right: "12mm" },
      });
      return Buffer.from(pdf);
    } finally {
      await browser.close();
    }
  } catch (err) {
    const message = err instanceof Error ? err.message : String(err);
    throw new Error(
      `PDF generation failed (Playwright/Chromium unavailable): ${message}`
    );
  }
}

// ─── XLSX generation (Compliance Matrix & BoQ) ───────────────────────────────

export async function generateComplianceMatrixXLSX(
  project: TenderProject,
  brand: BrandProfile | null,
  checks?: { framework: string; controlId: string; title: string; requirement: string; complianceLevel: string | null; status: string; evidence?: string | null }[]
): Promise<Buffer> {
  const wb = new ExcelJS.Workbook();
  wb.creator = "Arabclue";
  wb.created = new Date();

  const ws = wb.addWorksheet("Compliance Matrix", {
    views: [{ rightToLeft: false }],
    properties: { defaultRowHeight: 20 },
  });

  ws.mergeCells("A1:G1");
  const titleCell = ws.getCell("A1");
  titleCell.value = `Arabclue Compliance Matrix — ${project.title}`;
  titleCell.font = { size: 14, bold: true, color: { argb: "FFFFFFFF" } };
  titleCell.fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FF1E3A8A" } };
  titleCell.alignment = { vertical: "middle", horizontal: "center" };
  ws.getRow(1).height = 30;

  const headers = ["Framework", "Control ID", "Title", "Requirement", "Level", "Status", "Evidence"];
  headers.forEach((h, i) => {
    const cell = ws.getCell(2, i + 1);
    cell.value = h;
    cell.font = { bold: true, color: { argb: "FFFFFFFF" } };
    cell.fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FF0F172A" } };
    cell.alignment = { vertical: "middle", horizontal: "center" };
  });

  const tenderType = getTenderType(project.category);
  let row = 3;

  if (checks && checks.length > 0) {
    for (const c of checks) {
      ws.getCell(row, 1).value = c.framework;
      ws.getCell(row, 2).value = c.controlId;
      ws.getCell(row, 3).value = c.title;
      ws.getCell(row, 4).value = c.requirement;
      ws.getCell(row, 5).value = c.complianceLevel ?? "C1";
      ws.getCell(row, 6).value = c.status;
      ws.getCell(row, 7).value = c.evidence ?? "";
      const color =
        c.status === "COMPLIANT"
          ? "FF059669"
          : c.status === "NON_COMPLIANT" || c.status === "GAP"
            ? "FFDC2626"
            : "FFD97706";
      ws.getCell(row, 6).font = { bold: true, color: { argb: color } };
      row++;
    }
  } else {
    for (const fw of COMPLIANCE_FRAMEWORKS.filter(
      (f) => tenderType.complianceScope.includes(f.id) || f.id === "NORA"
    )) {
      for (const ctrl of fw.controls) {
        ws.getCell(row, 1).value = fw.id;
        ws.getCell(row, 2).value = ctrl.controlId;
        ws.getCell(row, 3).value = ctrl.title;
        ws.getCell(row, 4).value = ctrl.requirement;
        ws.getCell(row, 5).value = ctrl.level;
        ws.getCell(row, 6).value = "PENDING";
        ws.getCell(row, 7).value = "";
        row++;
      }
    }
  }

  ws.columns = [
    { width: 16 },
    { width: 14 },
    { width: 28 },
    { width: 45 },
    { width: 10 },
    { width: 12 },
    { width: 40 },
  ];
  ws.views = [{ state: "frozen", ySplit: 2 }];

  void brand;
  const buf = await wb.xlsx.writeBuffer();
  return Buffer.from(buf);
}

export async function generateBoQXLSX(
  project: TenderProject,
  brand: BrandProfile | null,
  boqItems?: {
    item: string;
    unit: string;
    qty: number;
    unitPrice: number | null;
    total: number | null;
  }[]
): Promise<Buffer> {
  const wb = new ExcelJS.Workbook();
  wb.creator = "Arabclue";
  wb.created = new Date();
  const ws = wb.addWorksheet("Financial BoQ");

  ws.mergeCells("A1:F1");
  const tc = ws.getCell("A1");
  tc.value = `Financial Bill of Quantities — ${project.title}`;
  tc.font = { size: 14, bold: true, color: { argb: "FFFFFFFF" } };
  tc.fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FF1E3A8A" } };
  tc.alignment = { vertical: "middle", horizontal: "center" };
  ws.getRow(1).height = 30;

  const headers = [
    "Item #",
    "Description",
    "Unit",
    "Qty",
    "Unit Price (SAR)",
    "Total (SAR)",
  ];
  headers.forEach((h, i) => {
    const cell = ws.getCell(2, i + 1);
    cell.value = h;
    cell.font = { bold: true, color: { argb: "FFFFFFFF" } };
    cell.fill = { type: "pattern", pattern: "solid", fgColor: { argb: "FF0F172A" } };
  });

  // Structure-only defaults — never invent prices from budget
  const items =
    boqItems && boqItems.length > 0
      ? boqItems
      : [
          { item: "Mobilization & Project Setup", unit: "LS", qty: 1, unitPrice: null, total: null },
          { item: "Design & Architecture", unit: "LS", qty: 1, unitPrice: null, total: null },
          { item: "Build & Implementation", unit: "LS", qty: 1, unitPrice: null, total: null },
          { item: "UAT, Training & Go-Live", unit: "LS", qty: 1, unitPrice: null, total: null },
          { item: "Support & Warranty (12 months)", unit: "LS", qty: 1, unitPrice: null, total: null },
        ];

  let row = 3;
  let subtotal = 0;
  let hasPrices = false;
  items.forEach((it, idx) => {
    ws.getCell(row, 1).value = idx + 1;
    ws.getCell(row, 2).value = it.item;
    ws.getCell(row, 3).value = it.unit;
    ws.getCell(row, 4).value = it.qty;
    if (it.unitPrice != null) {
      ws.getCell(row, 5).value = it.unitPrice;
      hasPrices = true;
    } else {
      ws.getCell(row, 5).value = "—";
    }
    if (it.total != null) {
      ws.getCell(row, 6).value = it.total;
      subtotal += it.total;
      hasPrices = true;
    } else {
      ws.getCell(row, 6).value = "—";
    }
    row++;
  });

  if (hasPrices && subtotal > 0) {
    const vat = Math.round(subtotal * 0.15 * 100) / 100;
    ws.getCell(row, 2).value = "Subtotal";
    ws.getCell(row, 6).value = subtotal;
    row++;
    ws.getCell(row, 2).value = "VAT 15%";
    ws.getCell(row, 6).value = vat;
    row++;
    ws.getCell(row, 2).value = "Grand Total (SAR)";
    ws.getCell(row, 2).font = { bold: true };
    ws.getCell(row, 6).value = subtotal + vat;
    ws.getCell(row, 6).font = { bold: true };
    row += 2;
  } else {
    row++;
  }
  ws.getCell(row, 2).value =
    "Prices are client-entered only. ArabClue does not suggest or calculate bid prices. Local content preference is an evaluation rule under the Government Tenders and Procurement Law.";

  ws.columns = [
    { width: 10 },
    { width: 40 },
    { width: 8 },
    { width: 8 },
    { width: 16 },
    { width: 14 },
  ];

  void brand;
  const buf = await wb.xlsx.writeBuffer();
  return Buffer.from(buf);
}

