// ─── ZIP packaging ───────────────────────────────────────────────────────────

export async function generateBidPackageZIP(
  proposal: GeneratedProposal,
  project: TenderProject,
  brand: BrandProfile | null
): Promise<Buffer> {
  const zip = new JSZip();
  const tenderType = getTenderType(project.category);

  // Technical Proposal (printable HTML / PDF)
  const pdf = await generateProposalPDF(proposal, project, brand);
  zip.file("Technical_Proposal.html", pdf);

  // Slides (HTML, printable)
  const slides = generateSlidesHTML(proposal, project, brand);
  zip.file("Technical_Proposal_Slides.html", slides);

  // Compliance Matrix XLSX
  const matrix = await generateComplianceMatrixXLSX(project, brand);
  zip.file("Compliance_Matrix.xlsx", matrix);

  // Financial BoQ XLSX
  const boq = await generateBoQXLSX(project, brand);
  zip.file("Financial_BoQ.xlsx", boq);

  // Raw markdown
  zip.file("Proposal_Content.md", proposal.contentMd ?? "");

  // README
  const readme = `Arabclue Bid Package
====================
Project: ${project.title}
Etimad Ref: ${project.etimadRef ?? "N/A"}
Tender Type: ${tenderType.name} / ${tenderType.nameAr}
Budget: ${project.currency} ${(project.budget ?? 0).toLocaleString()}
Generated: ${new Date().toISOString()}
Compliance Score: ${proposal.complianceScore ?? 100}%

Contents:
1. Technical_Proposal.html        — Full technical proposal (printable / Save as PDF)
2. Technical_Proposal_Slides.html — Executive presentation (printable)
3. Compliance_Matrix.xlsx         — Regulatory compliance matrix
4. Financial_BoQ.xlsx             — Bill of Quantities (Etimad format)
5. Proposal_Content.md            — Raw markdown source

PDPL Compliant · KSA Data Residency · Vision 2030 Aligned
Generated by Arabclue
`;
  zip.file("README.txt", readme);

  const buf = await zip.generateAsync({ type: "nodebuffer", compression: "DEFLATE", compressionOptions: { level: 9 } });
  return Buffer.from(buf);
}
