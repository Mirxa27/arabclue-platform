export type PdfLocale = "ar" | "en";

function resolveLocale(proposal: GeneratedProposal, override?: PdfLocale): PdfLocale {
  if (override === "ar" || override === "en") return override;
  const loc = (proposal as { locale?: string }).locale;
  return loc === "en" ? "en" : "ar";
}

